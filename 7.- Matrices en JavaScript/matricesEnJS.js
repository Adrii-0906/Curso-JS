// Matrices en JS
// Declaracion de una matriz
let matriz = [[],[]];

// Modificar valores de la matriz
// Elementos de la primera fila
matriz[0][0] = 100;
matriz[0][1] = 200;
matriz[0][2] = 300;

// Elementos de la segunda fila
matriz[1][0] = 400;
matriz[1][1] = 500;
matriz[1][2] = 600;


// Leer los valores de la matriz
console.log('Elemeto[0][1] = ' + matriz[0][1]);
console.log('Elemeto[1][2] = ' + matriz[1][2]);

// Asignar los elementos a una nueva matriz
let matriz2 = [[100, 200, 300], [400, 500, 600]]

// Leer los valores de la matriz
console.log('Elemeto[0][1] = ' + matriz2[0][1]);
console.log('Elemeto[1][2] = ' + matriz2[1][2]);